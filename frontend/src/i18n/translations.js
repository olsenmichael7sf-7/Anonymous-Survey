// 获取翻译文本的辅助函数
export const t = (key, language = 'zh', params = {}) => {
  let text = translations[language]?.[key] || translations.zh[key] || key;
  
  // 替换参数 {param}
  Object.keys(params).forEach(param => {
    text = text.replace(`{${param}}`, params[param]);
  });
  
  return text;
};

